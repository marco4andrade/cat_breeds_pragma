import '../../domain/entities/cat_breed.dart';
import '../../domain/entities/weight.dart';

class CatBreedModel extends CatBreed {
  CatBreedModel({
    required Weight weight,
    required String id,
    required String name,
    String? cfaUrl,
    String? vetstreetUrl,
    String? vcahospitalsUrl,
    required String temperament,
    required String origin,
    required String countryCodes,
    required String countryCode,
    required String description,
    required String lifeSpan,
    required int indoor,
    int? lap,
    required String altNames,
    required int adaptability,
    required int affectionLevel,
    required int childFriendly,
    required int dogFriendly,
    required int energyLevel,
    required int grooming,
    required int healthIssues,
    required int intelligence,
    required int sheddingLevel,
    required int socialNeeds,
    required int strangerFriendly,
    required int vocalisation,
    required int experimental,
    required int hairless,
    required int natural,
    required int rare,
    required int rex,
    required int suppressedTail,
    required int shortLegs,
    String? wikipediaUrl,
    required int hypoallergenic,
    String? referenceImageId,
    String? imageUrl,
  }) : super(
          weight: weight,
          id: id,
          name: name,
          cfaUrl: cfaUrl,
          vetstreetUrl: vetstreetUrl,
          vcahospitalsUrl: vcahospitalsUrl,
          temperament: temperament,
          origin: origin,
          countryCodes: countryCodes,
          countryCode: countryCode,
          description: description,
          lifeSpan: lifeSpan,
          indoor: indoor,
          lap: lap,
          altNames: altNames,
          adaptability: adaptability,
          affectionLevel: affectionLevel,
          childFriendly: childFriendly,
          dogFriendly: dogFriendly,
          energyLevel: energyLevel,
          grooming: grooming,
          healthIssues: healthIssues,
          intelligence: intelligence,
          sheddingLevel: sheddingLevel,
          socialNeeds: socialNeeds,
          strangerFriendly: strangerFriendly,
          vocalisation: vocalisation,
          experimental: experimental,
          hairless: hairless,
          natural: natural,
          rare: rare,
          rex: rex,
          suppressedTail: suppressedTail,
          shortLegs: shortLegs,
          wikipediaUrl: wikipediaUrl,
          hypoallergenic: hypoallergenic,
          referenceImageId: referenceImageId,
          imageUrl: imageUrl,
        );

  factory CatBreedModel.fromJson(Map<String, dynamic> json) {
    return CatBreedModel(
      weight: Weight(
        imperial: json['weight']['imperial'] ?? '',
        metric: json['weight']['metric'] ?? '',
      ),
      id: json['id'] ?? '',
      name: json['name'] ?? '',
      cfaUrl: json['cfa_url'],
      vetstreetUrl: json['vetstreet_url'],
      vcahospitalsUrl: json['vcahospitals_url'],
      temperament: json['temperament'] ?? '',
      origin: json['origin'] ?? '',
      countryCodes: json['country_codes'] ?? '',
      countryCode: json['country_code'] ?? '',
      description: json['description'] ?? '',
      lifeSpan: json['life_span'] ?? '',
      indoor: json['indoor'] ?? 0,
      lap: json['lap'],
      altNames: json['alt_names'] ?? '',
      adaptability: json['adaptability'] ?? 0,
      affectionLevel: json['affection_level'] ?? 0,
      childFriendly: json['child_friendly'] ?? 0,
      dogFriendly: json['dog_friendly'] ?? 0,
      energyLevel: json['energy_level'] ?? 0,
      grooming: json['grooming'] ?? 0,
      healthIssues: json['health_issues'] ?? 0,
      intelligence: json['intelligence'] ?? 0,
      sheddingLevel: json['shedding_level'] ?? 0,
      socialNeeds: json['social_needs'] ?? 0,
      strangerFriendly: json['stranger_friendly'] ?? 0,
      vocalisation: json['vocalisation'] ?? 0,
      experimental: json['experimental'] ?? 0,
      hairless: json['hairless'] ?? 0,
      natural: json['natural'] ?? 0,
      rare: json['rare'] ?? 0,
      rex: json['rex'] ?? 0,
      suppressedTail: json['suppressed_tail'] ?? 0,
      shortLegs: json['short_legs'] ?? 0,
      wikipediaUrl: json['wikipedia_url'],
      hypoallergenic: json['hypoallergenic'] ?? 0,
      referenceImageId: json['reference_image_id'],
      imageUrl: json['image'] != null ? json['image']['url'] : null,
    );
  }
}